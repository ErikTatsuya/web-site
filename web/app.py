from fastapi import FastAPI
from fastapi.middleware.cors import CORSMiddleware
import random


app = FastAPI()
app.add_middleware(
    CORSMiddleware,
    allow_origins=["*"],
    allow_credentials=True,
    allow_methods=["*"],
    allow_headers=["*"],
)


@app.get("/")

async def read_root():
    number = random.randint(1, 100)
    return {"random_number": number}
